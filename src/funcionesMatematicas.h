#ifndef FUNCIONESMATEMATICAS_H_
#define FUNCIONESMATEMATICAS_H_



#endif /* FUNCIONESMATEMATICAS_H_ */

/** \brief Suma dos numeros enteros
 *
 * \param a int Primer operando
 * \param b int Segundo operando
 * \return int Devuelve la suma de los dos operandos
 *
 */
int sumar(int a, int b);

/** \brief Resta dos numeros enteros
 *
 * \param a int Primer operando
 * \param b int Segundo operando
 * \return int Devuelve la resta de los dos operandos
 *
 */
int restar(int a, int b);

/** \brief Divide dos numeros enteros
 *
 * \param a int Primer operando
 * \param b int Segundo operando
 * \return float Devuelve el cociente de los dos operandos
 *
 */
float dividir(int a, int b);

/** \brief Multiplica dos numeros enteros
 *
 * \param a int Primer operando
 * \param b int Segundo operando
 * \return int Devuelve el producto de los dos operandos
 *
 */
int multiplicar(int a, int b);

/** \brief Calcula el factorial del primer operando
 *
 * \param a int Primer operando
 * \return int Devuelve el factorial del primer operando
 *
 */
int factorialA(int a);

/** \brief Calcula el factorial del segundo operando
 *
 * \param b int Primer operando
 * \return int Devuelve el factorial del primer operando
 *
 */
int factorialB(int b);

